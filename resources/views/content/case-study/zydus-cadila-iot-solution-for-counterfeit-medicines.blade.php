<p class="desc mb-0">Steps to Verify Your Medicines/Injections From Zydus.</p>
</div>
<div class="col-12 col-md-12 col-lg-12 col-xl-12">
<div class="row mt-5">
 <div class="col-12 col-md-12 col-lg-6 col-xl-6 mb-3">
 <div class="card p-4 h-100 d-flex flex-column">
<h3 class="sub-title">1. Register/Login</h3>
<p class="desc mb-0">This is the first step to sign up or log into your app. Here, the user must provide their valid contact number, where they will receive an OTP. It should be noted that this app is valid in India only, so the users must provide an Indian number only.</p>
<br>
<h3 class="sub-title">2. Enter Your OTP</h3>
<p class="desc mb-0">The next step is to enter the OTP number that the users will receive on their mobile once they enter their phone number. The OPT will expire within 3 minutes, so the users must enter the OTP within that time frame.</p>
</div>
</div>
<div class="col-12 col-md-12 col-lg-6 col-xl-6 mb-3">
<div class="card p-4 h-100 d-flex flex-column">
<img src="../../assets/case-study/zydus/scratch.png" width="100" alt="scratch">
<h3 class="sub-title mt-2">3. Scratch to Verify Your Medicine/Injection</h3>
<p class="desc mb-0">This is the final step. Here, the user must enter the scratch code placed on the medicine packaging to authenticate the medicine. This will tell them if the medicine is genuine or fake. It will also inform them if they already used that code or it's used by someone else.</p>
<br>
<h3 class="sub-title">4. Authenticate Your Medicine</h3>
<p class="desc mb-0">If the entered code is valid, a success pop-up will appear. And if it's not valid, a failure pop-up will appear. It aims to inform you if the medicine or injection is genuine or not.</p>

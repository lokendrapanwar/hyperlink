<p class="desc mb-0">Oauth is implemented for the app to authenticate the API request. We needed multiple Oauth implementations, one for APP, and the rest for admin. It was a challenging task to validate and destroy the token separately. However, we implemented one algorithm to resolve this issue and thus managed authentication.</p>

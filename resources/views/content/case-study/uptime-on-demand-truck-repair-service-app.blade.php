 <h2 class="main-title mb-3 pt-0 pt-md-5 services-title cs-project-approach-title c-black">Project Approach & Results</h2>
<p class="desc mb-0">We all know there are not many options available that allow repairing services to the truck drivers on their location, so we had a huge responsibility to provide an easy-to-use user interface so that any truck drivers can generate their repair requests effortlessly.<br><br>So, our aim for developing the mobile application was to resolve all the issues that truck drivers tend to face about the truck repairing while being on the journey using different frameworks and approaches. To begin with, we analyzed the concept hypothetically and created a file with the complete details covering all these points, diagrams, scenarios, problems, flow charts, and SRS for the entire workflow and planned the App development process. After finalizing the document, In the third phase, based on the client's requirements and our findings, we defined Mobile App architectures for UpTime App. After that, Our Mobile app designers started working on the prepared mobile app wire-frames and Application design. After getting approval on the final app design, our app developers move ahead with the development phase. We created the prototype for the UpTime app with all the native data, After the client's review and confirmation of the prototype. The Mobile app developers started their development process by choosing the best-fit technology for android, iOS, and the admin panel. We integrated APIs in the UpTime app and completed the whole app data flow.<br><br>We chose native technology swift for iOS development and Kotlin for android development and used JSON-based rest API development with Node and Express Framework. Hyperlink Infosystem developers used Angular 9 to create an admin panel for basic tasks such as managing user details, services, order details, etc. We have used web3 for calling smart contracts with the help of solidity. Our app developers had used Magic SDK for passwordless authentication providing secure login. As the main purpose of the mobile application is 
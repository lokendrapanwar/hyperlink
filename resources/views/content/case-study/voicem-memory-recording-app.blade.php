<p class="desc mb-0">The home screen will have various things such as the list of categories, recipients added, recording option, timeline by the recipients, banner and so on. Users can click on any to go to that section.</p>
<br>
<h3 class="sub-title">Category of the Memories</h3>
<p class="desc mb-0">Users have access to two types of memories; guided and freestyle memories.</p>

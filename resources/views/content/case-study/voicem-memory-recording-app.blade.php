<h2 class="main-title mb-3 mt-60 services-title cs-project-title c-black">Project Approach &amp; Results</h2>
<p class="desc mb-0">Once in our lifetime, we always wanted to keep a diary to keep track of our important memories. But on the other hand, we all know keeping a diary is never a smart or secure way to keep the track of memories. So, the client wanted a mobile app that allows the user to record and share the memories as voice notes to their loved ones which they can play whenever they want.
<br><br>Our core aim for the development of this mobile app was to allow the users to record their memories and share them with their loved ones to feel more connected and loved. To begin with, we analyzed the concept hypothetically and created a file with the complete details covering all these points, diagrams, scenarios, problems, flow charts, and SRS for the entire workflow and planned the Voicem mobile app development process. After finalizing the document, In the third phase, based on the client's requirements and our findings, we defined smart system architectures for the mobile app. After that, Our team of app designers started working to prepare the wire-frames and design of the system. After getting approval on the final app design, our team of app developers move ahead with the development phase.
<br><br>Our app developers started working on the development phase for the user-side panel. Next, they focused on the admin side panel. We created the prototype for the Voicem mobile app with all the native data. After the client's review and confirmation of the prototype; the mobile app developers started their development process by choosing the best-fit technology for android, and the admin panel. We integrated APIs in the Voicem app and completed the whole app data flow.
<br><br>We chose Kotlin as a primary language for android development and used JSON-based rest API development with Node and Laravel Framework. Hyperlink Infosystem developers used Angular 9 to create an admin panel for basic tasks such as managing reminders, user configuration, rating and reviews etc. We used web3 for calling the smart contracts with the help of solidity. Our app developers had used Magic SDK for passwordless authentication, providing secure login. As a mobile app to record memories is a unique idea, we wanted to provide the best user experience. Our app developers eliminated that with the help of a Matic network to deliver an exceptional user experience.
<br><br>From requirement gathering to deployment, the Voicem mobile app development journey was full of challenges. However, our dedication and teamwork led us to deliver the solution successfully, as well as within the prescribed timeline & budget. After the development phase, our QA team tested the mobile application before finally delivering it to the client.</p>

<p class="desc mb-0">The system verifies the entered mobile number of the user through an OTP verification process. The user can access their account only after a successful OTP verification process.</p>
<br>
<h3 class="sub-title">Profile Creation</h3>
<p class="desc mb-0">Users can provide their details such as name, gender, profile picture, city, country, age bracket and so on to complete their profile creation process.</p>

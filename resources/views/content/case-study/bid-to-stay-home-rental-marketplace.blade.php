<h2 class="main-title mb-3 mt-60 services-title cs-challenges-title c-black">Challenges</h2>
<p class="desc mb-1 c-black"><b>1. Auto Bidding Functionality Implementation</b></p>
<p class="desc mb-4">The primary concern of theBid To Stay mobile application is auto-bidding functionality. It was a challenge for us to task the last 4 possible Wednesdays from the booking date and autocomplete the bid on the very first Wednesday. For this, we had to check many scenarios as there are possible Wednesdays or not; another user has not booked property within the same time as well. Our App Developers worked tirelessly to work on this functionality and developed a solution that can handle the auto bid functionality perfectly.</p>
<p class="desc mb-1 c-black"><b>2. Booking Process Validations</b></p>
<p class="desc mb-0">The mobile app has various booking validations such as it should meet all the booking criteria such as the minimum number of guests, the property requires, the minimum number of stay days mentioned by the host, advance booking limits, reflecting the days the host has blocked for the booking and so on. It was surely tough and time-consuming for the app developers to validate these many scenarios in real-time, but they managed to do it anyway.</p>

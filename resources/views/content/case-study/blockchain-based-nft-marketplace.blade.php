<h2 class="main-title mb-3 mt-60 services-title cs-project-title c-black">Project Approach & Results</h2>
<p class="desc mb-0">As we are aware, blockchain is not the most scalable ecosystem currently. it always encounters problems like slow indexing at the time of creation, block size restrictions, multiple edition creation, etc. All these problems demand a unique solution to provide an impeccable user experience. So the client approached Hyperlink Infosystem with this great idea to develop a unique, secure and scalable NFT marketplace - *****.<br><br>So, our aim for developing this marketplace was to resolve all the mentioned issues using different frameworks and approaches. To begin with, we created a file with the complete details covering all these points, scenarios, problems, flow charts, and SRS. After finalizing the document, Our Mobile app designers started working on the app design phase. After getting approval on the final app design, our app developers move ahead with the development phase. The Mobile app developers started their development process by choosing the best-fit technology for android, iOS and the admin panel.<br><br>We chose native technology swift for iOS development and Kotlin for android development and used JSON-based rest API development with Node and Laravel Framework. Hyperlink Infosystem developers used Angular 9 to create an admin panel for basic tasks such as managing users, posts, reports, etc. We used web3 for calling the smart contracts with the help of solidity. Our app developers had used Magic SDK for passwordless authentication providing secure login. Any existing blockchain apps usually face the problem of poor user experience. Our app developers eliminated that with the help of a Matic network to deliver an exceptional user experience.<br><br>After the development phase, our QA team tested the mobile application before finally delivering it to the client.</p>

<p class="desc mb-0">Another application requirement that allows users to create a raffle for NFT, where users can buy a ticket for the NFT and announce the lottery winner after the designated time. Our developers encounter the challenge of creating an algorithm for the winner that they resolved by developing a custom algorithm specifically for the mobile application.</p>
